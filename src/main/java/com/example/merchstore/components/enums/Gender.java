package com.example.merchstore.components.enums;

/**
 * The Gender enum represents the gender of a user in the system.
 * It can have one of the following values:
 * <ul>
 *     <li>MALE: The user is male.</li>
 *     <li>FEMALE: The user is female.</li>
 *     <li>DEFAULT: The user's gender is not specified.</li>
 * </ul>
 *
 * @author Tomasz Zbroszczyk
 * @version 1.0
 * @since 31.05.2024
 */
public enum Gender {
    /**
     * Represents a male user.
     */
    MALE,

    /**
     * Represents a female user.
     */
    FEMALE,

    /**
     * Represents a user whose gender is not specified.
     */
    DEFAULT
}
